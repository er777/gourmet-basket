				<h3>

	Heading</h3>

<p>

	Nothing beats the smell of freshly baked cookies emanating from a warm oven ~ especially an oven in a beautiful log cabin nestled between an old pine forest and a lake so clear you can&#39;t tell how deep the water is. That&#39;s where you&#39;ll find Nikki Taylor doing what she does best, developing new, mouth-watering recipes for Nikki&rsquo;s Cookies &amp; Confections&reg;.</p>

<p>

	Nikki&rsquo;s Cookies &amp; Confections&reg; began with a time-honored, recipe from Blyborough, England; a recipe handed down to Nikki by her grandmother. For over 20 years we have enhanced this recipe adding fresh ingredients such a Meyer Lemons, Key Limes, Cranberries, Oranges and Pecans to add a taste of homemade goodness with a touch of excitement. Our assortment of tantalizing sweets now includes chocolate-layered cookies and holiday specialties like whimsical Gingerbread Cottages housing flavorful Gingerbread Boys and Bunny Boxes filled with Bunny Sugar Cookies.</p>

<p>

	Nikki uses only high quality, wholesome ingredients including Wisconsin farmer&#39;s butter. Most of her cookies are all natural; all are kosher, with no preservatives added. In fact, her cookies are so nourishing they are even welcomed in health-food stores and in the homes of families that want the homemade goodness of nutritious treats in their pantries.</p>

              
                   
